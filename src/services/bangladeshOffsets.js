// Helper function to get offset for a specific location
export const getBangladeshOffset = (cityName) => {
  // Try exact match first
  if (bangladeshOffsets[cityName]) {
    return bangladeshOffsets[cityName];
  }
  
  // Try case-insensitive match
  const normalizedCity = cityName.toLowerCase().trim();
  const match = Object.entries(bangladeshOffsets).find(
    ([key]) => key.toLowerCase() === normalizedCity
  );
  
  if (match) {
    return match[1];
  }
  
  // Try partial match for common variations
  const partialMatch = Object.entries(bangladeshOffsets).find(
    ([key]) => key.toLowerCase().includes(normalizedCity) || 
              normalizedCity.includes(key.toLowerCase())
  );
  
  if (partialMatch) {
    return partialMatch[1];
  }
  
  // Default to Dhaka offsets if no match
  return { sehri: 4, iftar: -3 };
};

